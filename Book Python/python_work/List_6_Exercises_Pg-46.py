Continents=["Africa","Asia","North America","South America","Antarctica","Australia","Europe"]
print(Continents)#original
print(sorted(Continents))#temporarily sorted
print(Continents)#original
print(sorted(Continents, reverse=True))#temperorily sorted(reversed)
print(Continents)#original
Continents.reverse()#reversed
print(Continents)
Continents.reverse()#back original
print(Continents)
Continents.sort()
print(Continents)
Continents.sort(reverse=True)#sorted permanently(reverse alphabatical)
print(Continents)